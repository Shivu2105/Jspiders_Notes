class narrow 
{
	//narrowing with help of type cast operator
	public static void main(String[] args) 
	{
		//int to byte
		int a = 1234;
		byte b =(byte)a;
		System.out.println(b);

		//short to byte
		short c = 345;
		byte d = (byte)c;
		System.out.println(c);

		//int to sort 
		int e = 1231;
		short f = (short)e;
		System.out.println(f);

		System.out.println("Hello World!");
	}
}
